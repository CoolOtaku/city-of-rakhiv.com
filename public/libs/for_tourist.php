                    (дані станом на 10.11.2020). Поїзд виїзжає на Рахів, та слідує через міста Львів, Івано-Франківськ та
                    прибуває до Рахова з самого ранку о 06 год 56 хв., вартість квитка, у вагоні люкс класу складе 1772 грн, у
                    вагоні купе від 726 грн, а у плацкарті від 390 грн в одну сторону (зверніть увагу, що вартість може
                    змінюватись, в залежності від дня купівлі квитків). У зворотньому напрямку, поїзд відправляється о 18.00 та
                    прибуває на залізничнй вокзал Київ о 9 год 55 хв.</li>
                <li>Якщо ви їдете з Харкова, то до Рахова ви можете доїхати поїздом 015 к. який відправляється з залізничного
